texto = 'Curso em Python' # String

print(texto.lower())  # Coloca Texto em Letras Minusculas
print(texto.upper())  # Coloca o Texto em Letras Maisculas
print(texto.title())  # Transforma o Texto em um Titulo onde as Primeiras letras serão Maiusculas
print(texto.capitalize()) # Coloca a Primeira Letra do texto  em Maiscula

