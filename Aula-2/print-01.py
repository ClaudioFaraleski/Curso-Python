'''

Dentro da função print podermos usar alguns argumentos dentro da função print

exemplos SEP e o END

Metodo sep é usado para separar letras e caracteres e numeros
Metodo end é usado para finalizar uma linha

Autor : Claudio Faraleski Junior
'''

print(1,2,3,4,5,6,7,8,sep='-')
print('ola','seja bem vindo','ao Curso de Python',sep='-')

print("#"*50)

print(1,2,3,4,5,6,7,8,sep='-',end='.')
print('ola','seja bem vindo','ao Curso de Python',sep='-',end=',')