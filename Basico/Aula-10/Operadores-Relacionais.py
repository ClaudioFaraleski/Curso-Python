numero = 49 # altere o valor da variavel e veja os resultados

if numero == 50 :
    print('Numero e Igual a 50')

if numero > 50:
    print('Maior  a 50')

if numero < 50:
    print('Menor que 50 ')

if numero >= 50:
    print('Maior ou igual a 50')

if numero <= 50:
        print('Menor ou igual a 50')

if numero != 50:
    print('Diferente de 50')

